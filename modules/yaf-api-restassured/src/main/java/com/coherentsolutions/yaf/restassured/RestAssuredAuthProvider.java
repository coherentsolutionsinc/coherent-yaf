import com.coherentsolutions.yaf.core.api.auth.AuthProvider;
import com.coherentsolutions.yaf.core.api.auth.AuthToken;
import io.restassured.specification.RequestSpecification;
import lombok.Data;
import lombok.EqualsAndHashCode;
import lombok.experimental.Accessors;

/**
 * The type Rest assured auth provider.
 */
@Data
@Accessors(chain = true)
@EqualsAndHashCode(callSuper = false)
public abstract class RestAssuredAuthProvider extends AuthProvider<RequestSpecification> {

    @Override
    protected void applyAuth(AuthToken token, RequestSpecification req) {
        req.header(AUTHORIZATION, BEARER + " " + token.getToken());
    }
}
